export function rand(a, b) {
  return Math.floor(Math.random() * (b - a + 1)) + a;
}
export function pick(arr) {
  return arr[rand(0, arr.length - 1)];
}

export function shuffle(a) {
  const c = [...a];
  for (let i = c.length - 1; i > 0; i--) {
    const j = rand(0, i);
    [c[i], c[j]] = [c[j], c[i]];
  }
  return c;
}

export function normalizeNames(t) {
  return [
    ...new Set(
      t
        .split("\n")
        .map((s) => s.trim())
        .filter(Boolean),
    ),
  ];
}

export function generateBalancedStats() {
  const budget = rand(420, 510);
  let vals = [];
  let remaining = budget;
  for (let i = 0; i < 7; i++) {
    const maxV = Math.min(95, remaining - (7 - i) * 20);
    const v = rand(20, Math.max(20, maxV));
    vals.push(v);
    remaining -= v;
  }
  vals.push(Math.max(20, Math.min(95, remaining)));
  vals = shuffle(vals);
  return {
    power: vals[0],
    speed: vals[1],
    hype: vals[2],
    chaos: vals[3],
    luck: vals[4],
    defense: vals[5],
    focus: vals[6],
    stamina: vals[7],
  };
}

export function buildFighterObjects(names) {
  return shuffle(names).map((name) => ({
    name,
    wins: 0,
    stats: generateBalancedStats(),
  }));
}

export function getEmoji(n) {
  if (emojiMap[n]) return emojiMap[n];
  const f = Object.entries(emojiMap).find(
    ([k]) => n.includes(k) || k.includes(n),
  );
  return f ? f[1] : "🧑‍💼";
}

export function spawnConfetti(n = 120) {
  const c = document.createElement("div");
  c.className = "confetti-container";
  document.body.appendChild(c);
  const cols = [
    "#ef4444",
    "#f59e0b",
    "#22c55e",
    "#3b82f6",
    "#a855f7",
    "#ec4899",
    "#facc15",
    "#06b6d4",
  ];
  for (let i = 0; i < n; i++) {
    const p = document.createElement("div");
    p.className = "confetti-piece";
    p.style.cssText = `left:${rand(0, 100)}%;width:${rand(6, 14)}px;height:${rand(6, 14)}px;background:${pick(cols)};border-radius:${rand(0, 1) ? "50%" : "2px"};animation-duration:${rand(20, 45) / 10}s;animation-delay:${rand(0, 25) / 10}s;`;
    c.appendChild(p);
  }
  setTimeout(() => c.remove(), 6000);
}
